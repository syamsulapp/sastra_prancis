                            <h3><a>The Education of a Foreign Correspondent</a></h3>
                            <div class="event-content">
                                <p>“These 977 individuals present an exceptional array of academic, extracurricular, and personal accomplishments,” said William R. Fitzsimmons, dean of admissions and financial aid. “Whatever institution they choose to attend, we are certain they will leave a lasting mark on the world.”
Last year, university’s Early Action program admitted 992 students to the Class of 2018. The year before, 892 were admitted, while 774 were admitted the year prior to that, when the program was restored following a four-year hiatus. While the number of students admitted through the program dropped slightly this year, Fitzsimmons said that early admission has become the “new normal” in many communities, and that many of the top students across the nation and around the globe continue to apply to colleges early.
First instituted in the mid-’70s, when university and other peer institutions had significantly smaller application pools, Early Action was suspended with the Class of 2012 amid concerns that students were making premature college choices and that the program gave an advantage to students who attended secondary schools with greater resources and better college counseling. The program was restored for the Class of 2016 following the global financial crisis, when it became clear that many students from low-income backgrounds were interested in the certainty its early financial aid awards provided.
“Increasing pressure on secondary school students, especially in early admission, has also long concerned us,” said Fitzsimmons. “For this reason, we make it clear that whether a student applies early or regular has no bearing on our ultimate admission decision.</p>
